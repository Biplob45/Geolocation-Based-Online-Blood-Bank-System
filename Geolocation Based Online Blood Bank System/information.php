	<p class="p_style">সুস্থ শরীরের জন্যে সঠিক রক্ত প্রবাহ প্রয়োজন। দুর্বল রক্ত প্রবাহের কারণে দেহের অন্যান্য অংশও ক্ষতিগ্রস্ত হতে পারে যেমন- মস্তিষ্ক, হার্ট, লিভার, কিডনি ও বিভিন্ন অঙ্গপ্রত্যঙ্গ। </p>
	<p class="p_style">আসুন জেনে নেই সাধারণ কিছু কথা যাতে সহজেই রক্ত প্রবাহ বাড়িয়ে নিজেকে সুস্থ রাখা যায় ।</p>
	<p class="p_style">১. প্রতিদিন অন্তত ৩০ মিনিট হাঁটুন । [বাসা থেকে বেরিয়ে কোন কাজের উদ্দেশ্যে হেঁটে রওনা দিতে পারেন অথবা শপিংএ বা বন্ধুদের আড্ডায় হেঁটে যেতে পারেন।]</p>
	<p class="p_style">২.সাধারণ কিছু হাত-পায়ের ব্যায়াম করুন ১৫ মিনিট ।</p>
