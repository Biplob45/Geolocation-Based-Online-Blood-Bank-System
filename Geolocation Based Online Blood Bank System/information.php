	<p class="p_style">বিপন্ন মানুষের মহামূল্যবান জীবনের সর্বোচ্চ নিরাপত্তা নিশ্চিত করতে পবিত্র কোরআনে ঘোষিত হয়েছে, ‘আর কেউ কারও প্রাণ রক্ষা করলে সে যেন পৃথিবীর সমগ্র মানবগোষ্ঠীকে প্রাণে রক্ষা করল।’ (সূরা আল-মায়িদা, আয়াত: ৩২)</p>
	  
