	<p class="p_style">হৃদরোগ ও হার্ট এটাকের ঝুঁকি কমে : সিএনএন পরিচালিত এক গবেষণায় দেখা যায়, রক্তে যদি লৌহের পরিমাণ বেশি থাকে তাহলে কোলেস্টেরলের অক্সিডেশনের পরিমাণ বেড়ে যায়, ধমনী ক্ষতিগ্রস্থ হয়, ফলাফল হৃদরোগের ঝুঁকি বৃদ্ধি। নিয়মিত রক্ত দিলে দেহে এই লৌহের পরিমাণ কমে যা হৃদরোগের ঝুঁকিকেও কমিয়ে দেয় কার্যকরীভাবে।</p>
	<p class="p_style">ফ্লোরিডা ব্লাড সার্ভিসের এক জরিপে দেখা গেছে, যারা নিয়মিত রক্ত দেন, তাদের হার্ট এটাকের ঝুঁকি অন্যদের চেয়ে ৮৮ ভাগ কম এবং স্ট্রোকসহ অন্যান্য মারাত্মক হৃদরোগের ঝুঁকি ৩৩ ভাগ কম।
</p>
	<p class="p_style">ক্যান্সারের ঝুঁকি কমে : মিলার-কিস্টোন ব্লাড সেন্টারের এক গবেষণায় দেখা যায়, নিয়মিত রক্ত দিলে ক্যান্সারের ঝুঁকি কমে। বিশেষ করে ফুসফুস, লিভার, কোলন, পাকস্থলী ও গলার ক্যান্সারের ঝুঁকি নিয়মিত রক্তদাতাদের ক্ষেত্রে অনেক কম বলে দেখা গেছে।</p>
	<p class="p_style">প্রাণবন্ততা ও কর্মক্ষমতা বৃদ্ধি : রক্তদান করার সাথে সাথে আমাদের শরীরের মধ্যে অবস্থিত বোন ম্যারো নতুন কণিকা তৈরির জন্যে উদ্দীপ্ত হয়। দান করার মাত্র ৪৮ ঘণ্টার মধ্যেই দেহে রক্তের পরিমাণ স্বাভাবিক হয়ে যায়, আর লোহিত কণিকার ঘাটতি পূরণ হতে সময় লাগে ৪ থেকে ৮ সপ্তাহ। আর এই পুরো প্রক্রিয়া আসলে শরীরের সার্বিক সুস্থতা, প্রাণবন্ততা আর কর্মক্ষমতাকেই বাড়িয়ে দেয়।</p>

